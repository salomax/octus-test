package sg.com.octus.test.salomax.common.to;

/**
 * Token transfer object.
 * 
 * @author salomax
 */
public class TokenTO {

	/**
	 * Token.
	 */
	private String token;
	
	/**
	 * Constructor.
	 */
	public TokenTO(String token) {
		this.token = token;
	}

	/**
	 * @return the token
	 */
	public String getToken() {
		return token;
	}

}
